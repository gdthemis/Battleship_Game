package sample.model;

public class Quartet {
    public int x;
    public int y;
    public boolean miss_or_hit;
    public int type;

    public Quartet(int x, int y, boolean miss_or_hit, int type) {
        this.x = x;
        this.y = y;
        this.miss_or_hit = miss_or_hit;
        this.type = type;
    }
}
